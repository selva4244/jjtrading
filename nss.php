   <div style="padding-left:5%;">            
<h4 style=" color: #F4A460; font-size: 20px;">Introduction</h4>
<p>The National Service Scheme (NSS) is an Indian Government-sponsored public service program conducted by the Ministry of Youth Affairs and Sports of the Government of India. Popularly known as NSS, the scheme was launched in Gandhiji's centenary year in 1969. Aimed at developing student's personality through community service, NSS is a voluntary association of young people in colleges, universities and at +2 level working for a campus-community linkage.</p>
<p>It inculcates the spirit of voluntary work among the students and teachers through sustained community interactions. It brings our academic institutions closer to society. It shows how to combine knowledge and action to achieve results, which are desirable for community development.</p><br>
<h4 style=" color: #F4A460; font-size: 20px;">Aim</h4>
<p>The programme aims to inculcate social welfare in students, and to provide service to society without bias. NSS volunteers work to ensure that everyone who is needy gets help to enhance their standard of living and lead a life of dignity. In doing so, volunteers learn from people in villages how to lead a good life despite a scarcity of resources.</p><br>
<h4 style=" color: #F4A460; font-size: 20px;">Motto</h4>
<h4  style=" color: #F4A460; font-size: 20px; text-align: center;">The motto of National Service Scheme is &ldquo;NOT ME BUT YOU&rdquo;</h4><br>
<h4 style="color: #F4A460; font-size:20px;">Objectives behind the NSS</h4>
<ul style="list-style-type: disc;">
<li><span>Enable the students to understand the community in which they work.</span></li>
<li><span>Understand themselves in relation to their community.</span></li>
<li><span>Identify the needs and problems in the community in the solution of which they can be involved.</span></li>
<li><span>Develop among themselves a sense of social and civic responsibility</span></li>
<li><span >Apply education- find practical solution individual &amp; community problems.</span></li>
<li><span >Develop competence required for group living and sharing responsibilities.</span></li>
<li><span >Gain skills in mobilising community participation.</span></li>
<li><span >Acquire leadership qualities and democratic attitude.</span></li>
<li><span >Develop capacity to meet emergencies and national disasters.</span></li>
<li><span >Practice national integration and social harmony.</span></li>
</ul>
