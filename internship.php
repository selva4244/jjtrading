<div style="padding-left:5%;">                
<h4 style="color:#F4A460; font-size:20px;">INTRODUCTION</h4><br>
<p style="color: #595959; ">The rise in global competition has prompted organizations to devise strategies to have a talented and innovative workforce to gain a competitive edge. Developing an internship policy is an impactful strategy for creating a future talent pool for the industry. The Internship program not only helps fresh pass-outs in gaining professional know-how but also benefits, corporate on fresh perspectives on business issues and even discovering future business leaders.Competition in the job sector is rising exponentially and securing entry-level jobs is getting very difficult, as the students passing out from technical institutions lack the experience and skills required by industry. </p><br>
<h4 style="color:#F4A460;;font-size: 20px;">OBJECTIVES</h4><br>
<p style="color: #595959; ">Internships are educational and career development opportunities, providing practical experience in a field or discipline. They are structured, short-term, supervised placements often focused around particular tasks or projects with defined timescales.. It is important that the objectives and the activities of the internship program are clearly defined and understood. Following are the intended objectives of internship training: </p>

<p style="color: #595959; ">• Will expose Technical students to the industrial environment, which cannot be simulated in the classroom and hence creating competent professionals for the industry.</p>
<p style="color: #595959; ">• Provide possible opportunities to learn, understand and sharpen the real time technical / managerial skills required at the job.</p>
<p style="color: #595959; ">• Exposure to the current technological developments relevant to the subject area of training.</p>
<p style="color: #595959; ">• Experience gained from the ‘Industrial Internship’ in classroom will be used in classroom discussions.</p>
<p style="color: #595959; ">• Create conditions conducive to quest for knowledge and its applicability on the job.</p>
<p style="color: #595959; ">• Gain experience in writing Technical reports/projects.</p>
<p style="color: #595959; ">• Expose students to the engineer’s responsibilities and ethics.</p>
<p style="color: #595959; "> • Familiarize with various materials, processes, products and their applications along with relevant aspects of quality control. </p>
<p style="color: #595959; ">• Promote academic, professional and/or personal development.</p>
<p style="color: #595959; "> • Expose the students to future employers.</p>
<p style="color: #595959; "> • Understand the social, economic and administrative considerations that influence the working environment of industrial organizations </p>
<p style="color: #595959; ">• Understand the psychology of the workers and their habits, attitudes and approach to problem solving.</p><br>

<h4 style="color:#F4A460;font-size: 20px;">BENEFITS OF INTERNSHIP</h4>
<p style="color: #595959; ">Benefits to the Indust</p>
<p style="color: #595959; ">• Availability of ready to contribute candidates for employment.</p>
<p style="color: #595959; ">• Year round source of highly motivated pre-professionals.</p>
<p style="color: #595959; ">• Students bring new perspectives to problem solving.</p>
<p style="color: #595959; ">• Visibility of the organization is increased on campus</p>
<p style="color: #595959; ">• Quality candidate’s availability for temporary or seasonal positions and projects.</p>
<p style="color: #595959; ">• Freedom for industrial staff to pursue more creative projects.</p>
<p style="color: #595959; ">• Availability of flexible, cost-effective work force not requiring a long-term employer commitment.</p>
<p style="color: #595959; ">• Proven, cost-effective way to recruit and evaluate potential employe</p>
<p style="color: #595959; "> • Enhancement of employer’s image in the community by contributing to the educational enterprise.</p><br>
<h4 style="color:#F4A460; font-size: 20px;">Benefits to Students</h4>
